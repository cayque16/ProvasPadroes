package questao3;

/**
 *
 * @author cayqu
 */
public abstract class Basico {
    private float cachaca = 100;
    private int gelo = 2;
    private float leiteCoco = 50;

    public float getCachaca() {
        return cachaca;
    }

    public void setCachaca(float cachaca) {
        this.cachaca = cachaca;
    }

    public int getGelo() {
        return gelo;
    }

    public void setGelo(int gelo) {
        this.gelo = gelo;
    }

    public float getLeiteCoco() {
        return leiteCoco;
    }

    public void setLeiteCoco(float leiteCoco) {
        this.leiteCoco = leiteCoco;
    }
    
    
}
